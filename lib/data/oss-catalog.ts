// =============================================================================
// TYPES
// =============================================================================

export type RepoType = 'core' | 'sdk' | 'template' | 'example' | 'tool' | 'mcp' | 'connector';
export type Maturity = 'stable' | 'active' | 'experimental';
export type DeploymentType = 'one-click' | 'docker' | 'k8s' | 'library' | 'npm' | 'pip';

export interface RepoActions {
  deployUrl?: string;
  docsUrl?: string;
  githubUrl: string;
  learnUrl?: string;
  npmUrl?: string;
  pypiUrl?: string;
}

export interface RepoSignals {
  updatedAt: string;
  featured: boolean;
  recommended: boolean;
  deployable: boolean;
  hotScore: number; // 0-100, computed from signals
}

export interface OSSRepo {
  id: string;
  name: string;
  slug: string;
  description: string;
  type: RepoType;
  categories: string[];
  languages: string[];
  useCases: string[];
  maturity: Maturity;
  deployment: DeploymentType[];
  actions: RepoActions;
  signals: RepoSignals;
  icon?: LucideIcon;
  upstream?: {
    name: string;
    url: string;
  };
}

export interface FeaturedCollection {
  id: string;
  title: string;
  description: string;
  repos: string[]; // repo ids
  icon: LucideIcon;
}

